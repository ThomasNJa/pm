package com.example.domain;

public class ConfigDomain {
	
	private String name;
	private String age;
	
	public ConfigDomain() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}
	
	
	
}
